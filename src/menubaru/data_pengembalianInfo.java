    private void bCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCariActionPerformed
        Object[] baris = {"id_pengembalian", "id_barang", "nama_barang", "jumlah_barang", "tanggal"};
        tabmode = new DefaultTableModel(null, baris);
        tablePengembalianInfo.setModel(tabmode);

        String keyword = cari.getText();
        String sql = "SELECT * FROM dataPengembalianInfo WHERE " +
                     "`id_pengembalian` LIKE ? OR " +
                     "`id_barang` LIKE ? OR " +
                     "`nama_barang` LIKE ? OR " +
                     "`jumlah_barang` LIKE ? OR " +
                     "`tanggal` LIKE ?";

        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            for (int i = 1; i <= 5; i++) {
                pst.setString(i, "%" + keyword + "%");
            }

            ResultSet hasil = pst.executeQuery();
            while (hasil.next()) {
                String a = hasil.getString("id_pengembalian");
                String b = hasil.getString("id_barang");
                String c = hasil.getString("nama_barang");
                String d = hasil.getString("jumlah_barang");
                String e = hasil.getString("tanggal");
                
                String[] data = {a, b, c, d, e};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal mencari data!\n" + e.getMessage());
        }
    }//GEN-LAST:event_bCariActionPerformed

    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal2 = sdf.format(tanggal.getDate());
        
        Object[] baris = {"id_pengembalian", "id_barang", "nama_barang", "jumlah_barang", "tanggal"};
        tabmode = new DefaultTableModel(null, baris);
        tablePengembalianInfo.setModel(tabmode);

        String sql = "INSERT INTO dataPengembalianInfo (id_pengembalian, id_barang, nama_barang, jumlah_barang, tanggal) VALUES (?, ?, ?, ?, ?)";

        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, idPengembalian.getText());
            ps.setString(2, idBarang.getText());
            ps.setString(3, namaBarang.getText());
            ps.setString(4, jumlahBarang.getText());
            ps.setString(5, tanggal2);            

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Pengembalian Barang Berhasil Disimpan");

            kosong();
            dataTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menyimpan Data Pengembalian Barang: " + e.getMessage());
        }
    }//GEN-LAST:event_bTambahActionPerformed

