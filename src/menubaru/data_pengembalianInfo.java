    private void tablePengembalianInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePengembalianInfoMouseClicked
        int baris = tablePengembalianInfo.getSelectedRow();
        if (baris != -1) {
            idPengembalian.setText(tabmode.getValueAt(baris, 0).toString());
            idBarang.setText(tabmode.getValueAt(baris, 1).toString());
            namaBarang.setText(tabmode.getValueAt(baris, 2).toString());
            jumlahBarang.setText(tabmode.getValueAt(baris, 3).toString());
            try {
                String tglString = tabmode.getValueAt(baris, 4).toString();
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                Date tgl = sdf.parse(tglString);
                tanggal.setDate(tgl);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_tablePengembalianInfoMouseClicked

