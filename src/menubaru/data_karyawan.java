    protected void dataTable(){
        Object[] baris = {"id_Karyawan", "nama_Karyawan", "tempat_Lahir", "tanggal_Lahir", "jenis_Kelamin", "divisi", "jabatan", "no_Telp", "alamat", "gambar"};
        tabmode = new DefaultTableModel(null, baris);
        tableDataKaryawan.setModel(tabmode);
        String sql = "SELECT * FROM dataKaryawan";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_Karyawan");
                String b = hasil.getString("nama_Karyawan");
                String c = hasil.getString("tempat_Lahir");
                String d = hasil.getString("tanggal_Lahir");
                String e = hasil.getString("jenis_Kelamin");
                String f = hasil.getString("divisi");
                String g = hasil.getString("jabatan");
                String h = hasil.getString("no_Telp");
                String i = hasil.getString("alamat");
                String j = hasil.getString("gambar");
                
                String[] data = {a, b, c, d, e, f, g, h, i, j};
                tabmode.addRow(data);
            }
        }catch (Exception e){
        }
    }
    
    protected void isiComboBoxDivisi(){
        try {
            String sql = "SELECT nama_divisi FROM dataDivisi";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

            while (rs.next()) {
                String divisi = rs.getString("nama_divisi");
                model.addElement(divisi);
            }

            divisi.setModel(model);
        }catch (Exception e) {}
    }
    
    protected void isiComboBoxJabatan(){
        try {
            String sql = "SELECT nama_jabatan FROM dataJabatan";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

            while (rs.next()) {
                String jabatan = rs.getString("nama_jabatan");
                model.addElement(jabatan);
            }

            jabatan.setModel(model);
        }catch (Exception e) {}
    }

