    private String generateIdPengembalian() {
        String idBaru = "PB001";
        try {
            String sql = "SELECT MAX(RIGHT(id_pengembalian, 3)) AS nomor FROM datapengembalianBarang";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                int nomor = rs.getInt("nomor") + 1;
                idBaru = String.format("PB%03d", nomor);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal generate ID Pengembalian: " + e.getMessage());
        }
        return idBaru;
    }
    
